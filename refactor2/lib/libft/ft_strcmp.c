#include "libft.h"

int	ft_strcmp(char *line, char *c)
{
	int	i;

	i = 0;
	while (line[i] && c[i])
	{
		if (line[i] != c[i])
			return (0);
		i++;
	}
	if (line[i] != c[i])
		return (0);
	return (1);
}
